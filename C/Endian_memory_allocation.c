#include <stdio.h>
#include <string.h>
         
//       _____________________________________________
//       | 00000000 | 00000000 | 00000000 | 10011000 |
//       ---------------------------------------------
//            3          2           1          0
//       _____________________________________________
//       | 00000000 | 00000000 | 00000010 | 00000001 |
//       ---------------------------------------------
//            3          2           1          0

int main()
{
    
    int n = 512;
    char *ch;
    ch = (char*)&n;    // typecasting
    
    ch[0] = 1;
    // ch[1] = 2;
    *(++ch) = 2; 
    printf("%d\n", n);
    printf("%d %d %d %d\n", ch, *ch, ch+1, *(ch+1));

    return 0;
}